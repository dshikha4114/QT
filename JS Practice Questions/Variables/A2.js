//Template literals are a feature in JavaScript that allow you to create strings that can contain embedded expressions and variables. They are enclosed by backticks (`) instead of single or double quotes. Here are some key features of template literals:
//Multi-line Strings: You can create strings that span multiple lines without using escape sequences.
//Variable Interpolation: You can embed variables and expressions directly within the string using the ${} syntax.
//Expressions: You can include any valid JavaScript expression within the ${} syntax.